package mc.sel.token;

/**
 * Defines types of tokens supported by EL.
 *
 * @author Milan Crnjak
 */
public enum TokenType {

    PLUS,
    MINUS,
    MUL,
    DIV,
    LEFT_PAREN,
    RIGHT_PAREN,
    LEFT_BRACKET,
    RIGHT_BRACKET,
    AND,
    OR,
    GT,
    GTE,
    LT,
    LTE,
    EQUAL,
    NOT_EQUAL,
    NOT,
    TRUE,
    FALSE,
    NULL,
    STRING,
    DOUBLE,
    INT,
    ARGSEP,
    ID,
    DOT,
    END

}
